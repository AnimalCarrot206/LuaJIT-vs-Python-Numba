def root_result_formatter(result):
    return str.format("КОРЕНЬ -> {}", result[0])


def linear_function_result_formatter(result):
    a, b = result[0][0], result[0][1]

    f1, f2 = a % 1 == 0 and "%d" or "%f", b % 1 == 0 and "%d" or "%f"
    base_string = str.format("ФУНКЦИЯ -> y = %sx + %s" % (f1, f2))

    return str.format(base_string % (a, b))


